class calculadora {

    companion object{
    fun soma(n1: Int, n2: Int): Int{

        return n1 + n2
    }
    fun subtra(n1: Int, n2: Int): Int{

        return n1 - n2
    }
    fun multip(n1: Int, n2: Int): Int{

        return n1 * n2
    }
    fun divisao(n1: Int, n2: Int): Int{

        return n1 / n2


    }
}
}